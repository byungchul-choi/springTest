package tpost.commCode.vo;

import tpost.egovcomm.vo.PagingDefaultVO;


@SuppressWarnings("serial")
public class commCdMgntVO extends PagingDefaultVO {   /*페이징 vo 확장시켜서 사용*/

	private String cfcd;
	private String cfcdNm;
	private String cfcdDesc;
	private String crtDate;
	private String mdfDate;
	private String crtr;
	private String amdr;

	public commCdMgntVO() {
	  }

	  public commCdMgntVO(String cfcd, String cfcdNm, String cfcdDesc) {
	    this.cfcd = cfcd;
	    this.cfcdNm = cfcdNm;
	    this.cfcdNm = cfcdDesc;
	    
	  }


  public String getCfcd() {
		return cfcd;
	}

	public void setCfcd(String cfcd) {
		this.cfcd = cfcd;
	}

	public String getCfcdNm() {
		return cfcdNm;
	}

	public void setCfcdNm(String cfcdNm) {
		this.cfcdNm = cfcdNm;
	}

	public String getCfcdDesc() {
		return cfcdDesc;
	}

	public void setCfcdDesc(String cfcdDesc) {
		this.cfcdDesc = cfcdDesc;
	}

	public String getCrtDate() {
		return crtDate;
	}

	public void setCrtDate(String crtDate) {
		this.crtDate = crtDate;
	}

	public String getMdfDate() {
		return mdfDate;
	}

	public void setMdfDate(String mdfDate) {
		this.mdfDate = mdfDate;
	}

	public String getCrtr() {
		return crtr;
	}

	public void setCrtr(String crtr) {
		this.crtr = crtr;
	}

	public String getAmdr() {
		return amdr;
	}

	public void setAmdr(String amdr) {
		this.amdr = amdr;
	}


	@Override
	public String toString() {
	  return "commCdMgntVO{" +
	      "id='" + cfcd + '\'' +
	      ", name='" + cfcdNm + '\'' +
	      '}';
	}

}
