package tpost.commUtil.vo;

public class commUtilVO{
	private String cfcd; 		/* 분류코드 */
	private String bascd; 		/* 기본코드 */
	private String bascdNm; 	/* 기본코드 명*/
	private String dtcd; 		/* 상세코드 */
	private String dtcdNm; 		/* 상세코드 명*/
	
	private String oganCd; 		/* 기관코드 */
	private String tmpltCd;		/* 템플릿 코드 */
	private String tmpltNm; 	/* 템플릿 명 */
	
	
	public String getCfcd() {
		return cfcd;
	}
	public void setCfcd(String cfcd) {
		this.cfcd = cfcd;
	}
	
	public String getBascd() {
		return bascd;
	}
	public void setBascd(String bascd) {
		this.bascd = bascd;
	}
	
	public String getBascdNm() {
		return bascdNm;
	}
	public void setBascdNm(String bascdNm) {
		this.bascdNm = bascdNm;
	}
	
	public String getDtcd() {
		return dtcd;
	}
	public void setDtcd(String dtcd) {
		this.dtcd = dtcd;
	}
	
	public String getDtcdNm() {
		return dtcdNm;
	}
	public void setDtcdNm(String dtcdNm) {
		this.dtcdNm = dtcdNm;
	}
	
	
	public String getOganCd() {
		return oganCd;
	}
	public void setOganCd(String oganCd) {
		this.oganCd = oganCd;
	}
	
	public String getTmpltCd() {
		return tmpltCd;
	}
	public void setTmpltCd(String tmpltCd) {
		this.tmpltCd = tmpltCd;
	}
	
	public String getTmpltNm() {
		return tmpltNm;
	}
	public void setTmpltNm(String tmpltNm) {
		this.tmpltNm = tmpltNm;
	}
		
}